void STMFLASH_ReadDouble(u32 ReadAddr,double* pBuffer,u32 NumToRead);   		//从指定地址开始读出指定长度的数据
u8 STMFlash_ReadDoubleLast(u32 ReadAddr,double* pBuffer,u32 NumToRead);
						   
#endif

















